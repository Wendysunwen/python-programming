''' 
Conversion of Data Type
Examples and Playground
'''

print("----------------")

# Decimal to Binary
x = bin(125)
print(x)
print(type(x))

print("----------------")

# Decimal to Hexadecimal
x = hex(125)
print(x)
print(type(x))

print("----------------")

# Binary to Decimal
x = 0b1101
print(x)
print(type(x))

print("----------------")

# Hexadecimal to Decimal
x = 0x1a
print(x)
print(type(x))

print("----------------")

# String as Integer
x = int("11")
print(x)
print(type(x))

print("----------------")

# String as Binary
x = int("11", 2)
print(x)
print(type(x))

print("----------------")

# String as Hexadecimal
x = int("11", 16)
print(x)
print(type(x))

print("----------------")

# Decimal to String
x = str(11)
print(x)
print(type(x))

print("----------------")

# Binary to String
x = str(0b11)
print(x)
print(type(x))

print("----------------")

